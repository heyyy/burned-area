    """Returns the correct geographic model to use for the boosted regression
       tree based on the specified path/row
    Description: Determines the boosted regression model for the specified
        path/row.  Model is read from the hash table.
        
    History:
      Created on December 6, 2013 by Gail Schmidt, USGS/EROS LSRD Project
        
    Args:
      path - integer path value for the scene
      row - integer row value for the scene
   
    Returns:
      'invalid' - error obtaining the model name
      model_name - name of the geographic model to be used for boosted
          regression; a value of 'invalid' means the model does not exist
          for the specified path/row.
    """

    # get the key for the hash table from the path/row combination, then
    # pull the model name from that hash table using the key.  if the key
    # doesn't exist, then return 'invalid'.
    key = '%03d%03d' % (path, row)
    if key in model_hash:
        return model_hash[key]
    else:
        return 'invalid'

